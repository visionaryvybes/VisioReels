export type OverlayMode =
  | 'solid-bottom'   // opaque block pinned to the bottom third
  | 'gradient'       // top-to-bottom darkening gradient
  | 'framed'         // thin outlined rectangle containing text
  | 'split'          // image left / text panel right (or bottom for portrait)
  | 'full-bleed'     // text on image, no overlay treatment
  | 'vignette'       // radial darken at the edges
  | 'marquee';       // horizontal stripe across the middle

export type Alignment = 'start' | 'center' | 'end';

export interface SlidePreset {
  id: string;
  label: string;          // capslock in UI
  blurb: string;          // one-line human description
  accent: string;         // default accent hex
  titleFontVar: string;   // css var() reference
  bodyFontVar: string;
  titleWeight: number;
  bodyWeight: number;
  titleCase: 'upper' | 'lower' | 'normal';
  bodyCase: 'upper' | 'lower' | 'normal';
  titleTracking: string;  // e.g. '-0.02em'
  bodyTracking: string;
  align: Alignment;
  overlay: OverlayMode;
  surface: string;        // overlay colour — hex or rgba
  ink: string;            // text colour
  imageFilter?: string;   // optional CSS filter for image
  titleSize: number;      // relative to a 1080-wide canvas
  bodySize: number;
  leadingTitle: number;   // line-height
  leadingBody: number;
  maxTitleChars: number;  // agent contract: cap title length for this preset
  maxBodyChars: number;
}

