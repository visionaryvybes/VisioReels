// Typography + layout systems for the Slides/Carousel generator.
// Each preset is a self-contained visual language: font stack, weight,
// alignment, case, and overlay treatment. Designed to feel distinct the
// moment you switch between them.

export type SlideAspect = '1:1' | '4:5' | '9:16' | '16:9' | 'original';

